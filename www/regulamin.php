<H1>Regulamin korzystania z programu IT Inwentaryzator</H1>
<p><h2>1. Postanowienia ogólne i definicje</h2>
<p align="left">Nazwy użyte w Regulaminie należy rozumieć następująco:</br></p>
<li><b>IT Inwentaryzator</b> – program komputerowy umożliwiający zarządzanie informacjami o sprzęcie i oprogramowaniu przypisanych do pracownika.</li>
<li><b>Administrator Systemu</b> – osoba odpowiedzialna za nadzorowanie stosowania środków technicznych i organizacyjnych zapewniających ochronę przetwarzanych danych, w tym w szczególności za przeciwdziałanie dostępowi osób trzecich do systemu za podejmowanie odpowiednich działań w przypadku wykrycia naruszeń bezpieczeństwa tych danych.</li>
<li><b>Dane osobowe</b> – wszelkie informacje dotyczące osoby fizycznej. Osobą możliwą do zidentyfikowania jest osoba, której tożsamość można określić bezpośrednio lub pośrednio, w szczególności przez powołanie się na numer identyfikacyjny, imię lub nazwisko.</li>
<li><b>Dane o sprzęcie i oprogramowaniu</b> – wszelkie informacje dotyczące sprzętu teleinformatycznego i oprogramowania znajdującego się w zasobach firmy.</li>
<li><b>Użytkownik Systemu</b> – osoba upoważniona do bezpośredniego dostępu do danych przetwarzanych w systemie.</li>
<li><b>Przetwarzanie danych</b> - jakiekolwiek operacje wykonywane na danych, takie jak zbieranie, utrwalanie, przechowywanie, opracowywanie, zmienianie, udostępnianie i usuwanie.</li>
<li><b>Konto></b> – zbiór uprawnień nadawanych użytkownika systemu w celu podglądu lub modyfikacji przechowywanych danych.</li>
<li><b>Login</b> – formalna nazwa konta nadawana podczas procesu rejestracji.</li>
<li><b>Hasło</b> – ciąg znaków zdefiniowany przez Użytkownika Systemu zgodny z zasadami tworzenia haseł służący zalogowaniu się do systemu.</li>

<p><h2>2. Uzyskanie dostępu do systemu IT Inwentaryzator</h2>
<p align="left">1. W celu uzyskania konta w systemie użytkownik zobowiązany jest do wypełnienia formularza a następnie przesłania go drogą administracyjną do administratora systemu</p>
<p align="left">2. Użytkownik zobowiązany jest do wyboru loginu oraz ustalenia bezpiecznego hasła dostępu</p>
<p align="left">3. Poprzez bezpieczne hasło dostępu rozumie się hasło zawierające co najmniej jedną cyfrę oraz znak specjalny, nie dające się zbyt łatwo odgadnąć przez osoby trzecie</p>
<p align="left">4. W przypadku zapomnienia hasła użytkownik zobowiązany jest do jak najszybszego kontaktu z administratorem systemu.</p>
<p align="left">5. Przed oddaleniem się od komputera użytkownik powinien upewnić się, że ekran został zablokowany</p>
<p align="left">6. Dostęp do aplikacji może zostać uchylony na prośbę użytkownika lub przy rozwiązywaniu z nim stosunku pracy.</p>
<p align="left">Zabronione jest podejmowanie prób przejęcia przez użytkownika wyższych uprawnień niż te nadane przez administratora.</p>

<p><h2>3. Poufność i bezpieczeństwo danych</h2>
<p align="left">1. Użytkownik systemu zobowiązuje się do zachowania poufności względem pozyskiwanych i wprowadzanych danych oraz do nieudostępniania ich podmiotom trzecim.</p>
<p align="left">2. Użytkownikowi przyznawane są minimalne możliwe uprawnienia dostępu pozwalające na korzystanie z systemu zgodnie z zakresem pełnionych obowiązków.</p>
<p align="left">3. Zabrania się przechowywania haseł w ogólnodostępnych miejscach oraz udostępniania hasł osobom nie związanym z obsługą systemu.</p>
<p align="left">4. Administrator zobowiązuje się do cyklicznego sporządzania kopii zapasowej bazy danych firmowego sprzętu komputerowego oraz oprogramowania celem zapewnienia większej niezawodności działania systemu.</p>
<p align="left">5. Administrator prowadzi monitorowania i tworzenia dzienników zdarzeń w celu umożliwienia rejestracji działań związanych z bezpieczeństwem przetwarzania danych osobowych.</p>

<p><h2>4. Warunki korzystania z systemu</h2>
<p align="left">1. System IT Inwentaryzator dostepny jest tylko i wyłącznie dla zarejestrowanych użytkowników.</p>
<p align="left">2. Korzystanie z systemu możliwe jest jedynie w siedzibie organizacji, wyłącnie na zaaprobowanym sprzęcie komputerowym.</p>
<p align="left">3. Administrator systemu zobowiązuje się do informowania użytkowników drogą elektroniczną o planowanej konserwacji lub o awariach systemu</p>


<p><h2>5. Postanowienia końcowe</h2>
<p align="left">1. Dostęp do systemu zagwarantowany jest w zakresie i na zasadach określonych w Regulaminie.
</p>
<p align="left">2. Niniejsze postanowienia użytkownik akceptuje poprzez zaznaczenie "Akceptuj regulamin" na formularzu rejestracyjnym oraz przez wysłanie formularza</p>
<p align="left">3. Zastrzega się prawo do zmiany niniejszego regulaminu</p>

